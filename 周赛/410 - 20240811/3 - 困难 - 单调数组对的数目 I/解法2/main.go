func countOfPairs(nums []int) int {
	const MOD, MAX_VAL = 1000000007, 50

	n := len(nums)
	if n == 1 {
		return nums[0] + 1
	}

	dp := make([][][]int, n)
	for i := range dp {
		dp[i] = make([][]int, MAX_VAL+1)
		for j := range dp[i] {
			dp[i][j] = make([]int, MAX_VAL+1)
		}
	}

	var checkFunc func(index, last1, last2 int) int
	checkFunc = func(index, last1, last2 int) int {
		if index == n {
			return 1
		}
		if dp[index][last1][last2] != 0 {
			return dp[index][last1][last2]
		}

		counter := 0
		for v1 := max(last1, nums[index]-last2); v1 <= nums[index]; v1++ {
			v2 := nums[index] - v1
			counter += checkFunc(index+1, v1, v2)
			if counter >= MOD {
				counter -= MOD
			}
		}

		dp[index][last1][last2] = counter
		return counter
	}

	counter := 0
	for v1 := 0; v1 <= nums[0]; v1++ {
		v2 := nums[0] - v1
		counter += checkFunc(1, v1, v2)
		if counter >= MOD {
			counter -= MOD
		}
	}

	return counter
}
